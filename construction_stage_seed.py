def _get_stage_ids(cursor):
    """Получить stage_ids для всех этапов."""
    stage_ids = {}
    for row in cursor.execute(
        "SELECT id, building_type, name FROM construction_stage_templates"
    ).fetchall():
        stage_ids[(row["building_type"], row["name"])] = row["id"]
    return stage_ids


def _get_substage_ids(cursor):
    """Получить substage_ids: (building_type, stage_name, substage_name) -> id."""
    substage_ids = {}
    for row in cursor.execute(
        """SELECT csst.id as substage_id, cst.building_type, cst.name as stage_name, csst.name as substage_name
           FROM construction_substage_templates csst
           JOIN construction_stage_templates cst ON cst.id = csst.stage_template_id"""
    ).fetchall():
        substage_ids[(row["building_type"], row["stage_name"], row["substage_name"])] = row["substage_id"]
    return substage_ids


def migrate_fundament_substages(cursor, building_type: str):
    """Добавить подэтапы и операции для Фундамент (gasblock или frame) в существующих БД."""
    stage_ids = _get_stage_ids(cursor)
    substage_ids = _get_substage_ids(cursor)
    stage_id = stage_ids.get((building_type, "Фундамент"))
    if not stage_id:
        return

    # Добавляем только отсутствующие подэтапы
    for (bt, stage_name), sub_name, order_num in SUBSTAGE_TEMPLATES:
        if bt != building_type or stage_name != "Фундамент":
            continue
        key = (bt, stage_name, sub_name)
        if key in substage_ids:
            continue
        cursor.execute(
            """INSERT INTO construction_substage_templates (stage_template_id, name, order_num)
               VALUES (?, ?, ?)""",
            (stage_id, sub_name, order_num),
        )
        substage_ids[key] = cursor.lastrowid

    # Добавляем только отсутствующие операции
    for (bt, stage_name, sub_name), op_name, order_num, cw_title, cw_items, ctrl, errors, quick in OPERATION_TEMPLATES:
        if bt != building_type or stage_name != "Фундамент":
            continue
        substage_id = substage_ids.get((bt, stage_name, sub_name))
        if not substage_id:
            continue
        # Проверяем, есть ли уже такая операция
        existing = cursor.execute(
            "SELECT id FROM construction_operation_templates WHERE substage_template_id = ? AND name = ?",
            (substage_id, op_name),
        ).fetchone()
        if existing:
            continue
        cursor.execute(
            """INSERT INTO construction_operation_templates
               (substage_template_id, name, order_num, check_what_title, check_what_items, control_items, typical_errors, quick_checklist)
               VALUES (?, ?, ?, ?, ?, ?, ?, ?)""",
            (substage_id, op_name, order_num, cw_title or "", cw_items or "", ctrl or "", errors or "", quick or ""),
        )


