    if cursor.fetchone()[0] > 0:
        migrate_fundament_substages(cursor, "gasblock")
        migrate_fundament_substages(cursor, "frame")
        return  # этапы уже есть, только миграция подэтапов

    stage_ids = {}  # (building_type, stage_name) -> id
